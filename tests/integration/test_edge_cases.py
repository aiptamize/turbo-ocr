# --- method and protocol --------------------------------------------------

class TestProtocolEdges:
    def test_ocr_batch_empty_list(self, server_url):
        r = requests.post(f"{server_url}/ocr/batch", json={"images": []}, timeout=10)
        assert r.status_code in (200, 400)

    def test_ocr_batch_wrong_field(self, server_url):
        r = requests.post(f"{server_url}/ocr/batch", json={"wrong": []}, timeout=10)
        assert r.status_code == 400

    def test_unknown_pdf_mode(self, server_url, fixtures_dir):
        pdf = fixtures_dir / "pdf" / "simple_letter.pdf"
        if not pdf.exists():
            pytest.skip()
        r = requests.post(
            f"{server_url}/ocr/pdf?mode=nonsense",
            data=pdf.read_bytes(),
            headers={"Content-Type": "application/pdf"},
            timeout=30,
        )
        # Either 400 or fallback to default — both acceptable, just no crash.
        assert r.status_code in (200, 400)
