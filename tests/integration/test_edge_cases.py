"""Edge-case / outlier integration tests across all endpoints.

Uses fixtures generated by tests/fixtures/generate_edge_cases.py.
Assertions focus on crash-freeness, status codes, and schema shape —
NOT accuracy (edge cases intentionally have no ground truth).
"""

import pytest
import requests


# --- helpers ---------------------------------------------------------------

def _post_raw(server_url, data, content_type="image/png", timeout=30):
    return requests.post(
        f"{server_url}/ocr/raw",
        data=data,
        headers={"Content-Type": content_type},
        timeout=timeout,
    )


def _post_pdf(server_url, data, mode="ocr", timeout=120):
    return requests.post(
        f"{server_url}/ocr/pdf?mode={mode}",
        data=data,
        headers={"Content-Type": "application/pdf"},
        timeout=timeout,
    )


def _post_pixels(server_url, data, w, h, ch, timeout=10):
    return requests.post(
        f"{server_url}/ocr/pixels",
        data=data,
        headers={
            "X-Width": str(w),
            "X-Height": str(h),
            "X-Channels": str(ch),
        },
        timeout=timeout,
    )


# --- zero-byte / empty bodies ---------------------------------------------

class TestZeroByte:
    def test_raw_zero_byte(self, server_url, edge_cases_dir):
        r = _post_raw(server_url, (edge_cases_dir / "zero_byte.bin").read_bytes())
        assert r.status_code == 400

    def test_pdf_zero_byte(self, server_url, edge_cases_dir):
        r = _post_pdf(server_url, (edge_cases_dir / "zero_byte.bin").read_bytes())
        assert r.status_code == 400

    def test_pixels_zero_byte(self, server_url):
        r = _post_pixels(server_url, b"", 1, 1, 3)
        assert r.status_code == 400


